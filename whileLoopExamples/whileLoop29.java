package whileLoopExamples;

public class whileLoop29 {
    public static void main(String args[]){
        int i=1;
        while (i<=5){
            System.out.print(i+" ");
            i++;

        }
        int num=4;
        while(num>=1){
            System.out.print(num+" ");
            num--;
        }
    }
}